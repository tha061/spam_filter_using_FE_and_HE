inline std::vector<std::vector<double>> read_csv_file(std::string filename)
{
    // Reads a CSV file into a vector of <string, vector<int>> pairs where
    // each pair represents <column name, column values>

    // Create a vector of <int vector> pairs to store the result
    std::vector<std::vector<double>> result;

    // Create an input filestream
    std::ifstream myFile(filename);

    // Make sure the file is open
    if(!myFile.is_open()) throw std::runtime_error("Could not open file");

    // Helper vars
    std::string line, colname;
    double val;

    // Read data, line by line
    
    while(std::getline(myFile, line))
    {
        // cout<<"line = "<<line<<endl;;
        // Create a stringstream of the current line
        std::stringstream ss(line);
        
        std::vector<double> tmp_vector;
        
        // Keep track of the current column index
        int colIdx = 0;
        // Extract each integer
        while(ss >> val)
        {
            
            // Add the current integer to the 'colIdx' column's values vector
            // std::cout<<"colIdx= "<<colIdx<<std::endl;
            // std::cout<<"val= "<<val<<std::endl;
            // tmp_vector[colIdx]=val;
            tmp_vector.push_back(val);
            // cout<<"test ok"<<endl;
            // result.at(colIdx).push_back(val);
            
            // If the next token is a comma, ignore it and move on
            if(ss.peek() == ',') 
            {   
                // cout<<"found ','"<<endl;
                ss.ignore();
            }
            // Increment the column index
            colIdx++;
        }
        result.push_back(tmp_vector);
    }

    // Close file
    myFile.close();

    return result;
}

inline double timeEvaluate(string task_name, high_resolution_clock::time_point t1, high_resolution_clock::time_point t2)
{
	double time_diff = duration_cast<milliseconds>(t2 - t1).count();
	return time_diff;
}

inline void trackTaskPerformance(TRACK_LIST &time_track_list, string task_name, high_resolution_clock::time_point t1, high_resolution_clock::time_point t2)
{
	double task_time_diff = timeEvaluate(task_name, t1, t2);
	time_track_list.push_back(make_pair(task_name, to_string(task_time_diff)));
}


